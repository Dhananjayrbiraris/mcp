# ---------------------------------------------------------------------------
# Dispatcher
# ---------------------------------------------------------------------------


async def _dispatch(
    name: str,
    args: dict[str, Any],
    es: ElasticsearchClient,
    config: ElasticsearchConfig,
) -> Any:
    # -- Cluster / index info -----------------------------------------------
    if name == "es_ping":
        alive = await es.ping()
        if alive:
            info = await es.cluster_info()
            return {
                "status": "connected",
                "cluster_name": info.get("cluster_name"),
                "version": info.get("version", {}).get("number"),
            }
        return {"status": "unreachable"}

    if name == "es_list_indices":
        return await es.list_indices(args.get("pattern", "*"))

    if name == "es_get_mapping":
        return await es.get_mapping(args["index"])

    if name == "es_list_ml_models":
        return await es.get_ml_models()

    if name == "es_model_stats":
        return await es.model_deployment_stats(args["model_id"])

    # -- Semantic search ----------------------------------------------------
    if name == "es_semantic_search_elser":
        body = build_elser_query(
            query_text=args["query"],
            field=args["field"],
            model_id=args.get("model_id", config.default_model_id),
            filter_=args.get("filter"),
            source_fields=args.get("source_fields"),
            highlight_field=args.get("highlight_field"),
        )
        size = args.get("size", config.default_top_k)
        resp = await es.execute_search(args["index"], body, size=size)
        hits = parse_hits(resp, min_score=args.get("min_score", config.default_min_score))
        return {
            "total": resp.get("hits", {}).get("total", {}).get("value", 0),
            "hits": hits,
        }

    if name == "es_semantic_search_knn":
        body = build_knn_query(
            query_text=args["query"],
            field=args["field"],
            model_id=args.get("model_id", config.default_dense_model_id),
            k=args.get("k", config.default_top_k),
            num_candidates=args.get("num_candidates", config.default_num_candidates),
            filter_=args.get("filter"),
            source_fields=args.get("source_fields"),
        )
        resp = await es.execute_search(args["index"], body, size=args.get("k", config.default_top_k))
        hits = parse_hits(resp, min_score=0.0)
        return {
            "total": resp.get("hits", {}).get("total", {}).get("value", 0),
            "hits": hits,
        }

    if name == "es_semantic_search_hybrid":
        body = build_hybrid_query(
            query_text=args["query"],
            semantic_field=args["semantic_field"],
            keyword_fields=args["keyword_fields"],
            model_id=args.get("model_id", config.default_model_id),
            semantic_boost=args.get("semantic_boost", 1.0),
            keyword_boost=args.get("keyword_boost", 0.5),
            filter_=args.get("filter"),
            source_fields=args.get("source_fields"),
            highlight_field=args.get("highlight_field"),
        )
        size = args.get("size", config.default_top_k)
        resp = await es.execute_search(args["index"], body, size=size)
        hits = parse_hits(resp, min_score=args.get("min_score", config.default_min_score))
        return {
            "total": resp.get("hits", {}).get("total", {}).get("value", 0),
            "hits": hits,
        }

    if name == "es_semantic_search_rrf":
        body = build_rrf_query(
            query_text=args["query"],
            dense_field=args["dense_field"],
            keyword_fields=args["keyword_fields"],
            model_id=args.get("model_id", config.default_dense_model_id),
            k=args.get("k", config.default_top_k),
            num_candidates=args.get("num_candidates", config.default_num_candidates),
            rrf_rank_constant=args.get("rrf_rank_constant", 60),
            rrf_window_size=args.get("rrf_window_size", 100),
            filter_=args.get("filter"),
            source_fields=args.get("source_fields"),
        )
        size = args.get("size", config.default_top_k)
        resp = await es.execute_search(args["index"], body, size=size)
        hits = parse_hits(resp, min_score=0.0)
        return {
            "total": resp.get("hits", {}).get("total", {}).get("value", 0),
            "hits": hits,
        }

    if name == "es_semantic_search_native":
        body = build_semantic_text_query(
            query_text=args["query"],
            field=args["field"],
            filter_=args.get("filter"),
            source_fields=args.get("source_fields"),
            highlight_field=args.get("highlight_field"),
        )
        size = args.get("size", config.default_top_k)
        resp = await es.execute_search(args["index"], body, size=size)
        hits = parse_hits(resp, min_score=args.get("min_score", config.default_min_score))
        return {
            "total": resp.get("hits", {}).get("total", {}).get("value", 0),
            "hits": hits,
        }

    # -- Index / pipeline setup --------------------------------------------
    if name == "es_setup_elser_index":
        index = args["index"]
        text_field = args.get("text_field", "content")
        sparse_field = args.get("sparse_field", "content_embedding")
        model_id = args.get("model_id", config.default_model_id)
        pipeline_id = args.get("pipeline_id", "elser-embedding-pipeline")
        extra_fields = args.get("extra_fields")

        # Create pipeline
        pid, pbody = elser_ingest_pipeline(pipeline_id, text_field, sparse_field, model_id)
        pipeline_resp = await es.create_ingest_pipeline(pid, pbody)

        # Create index
        mapping = elser_index_mapping(text_field, sparse_field, extra_fields)
        index_resp = await es.create_index(index, mapping)

        return {
            "index": index_resp,
            "pipeline": pipeline_resp,
            "next_steps": (
                f"Index documents with pipeline='{pipeline_id}'. "
                f"Then search with es_semantic_search_elser using field='{sparse_field}'."
            ),
        }

    if name == "es_setup_dense_index":
        index = args["index"]
        text_field = args.get("text_field", "content")
        vector_field = args.get("vector_field", "content_vector")
        model_id = args.get("model_id", config.default_dense_model_id)
        pipeline_id = args.get("pipeline_id", "dense-embedding-pipeline")
        extra_fields = args.get("extra_fields")
        similarity = args.get("similarity", "cosine")

        dims = args.get("dims") or get_model_dims(model_id)
        if not dims:
            raise ValueError(
                f"Cannot auto-detect dims for model '{model_id}'. "
                "Please provide 'dims' explicitly."
            )

        pid, pbody = dense_ingest_pipeline(pipeline_id, text_field, vector_field, model_id)
        pipeline_resp = await es.create_ingest_pipeline(pid, pbody)

        mapping = dense_index_mapping(text_field, vector_field, dims, similarity, extra_fields)
        index_resp = await es.create_index(index, mapping)

        return {
            "index": index_resp,
            "pipeline": pipeline_resp,
            "dims": dims,
            "next_steps": (
                f"Index documents with pipeline='{pipeline_id}'. "
                f"Then search with es_semantic_search_knn using field='{vector_field}'."
            ),
        }

    if name == "es_create_ingest_pipeline":
        return await es.create_ingest_pipeline(args["pipeline_id"], args["pipeline_body"])

    if name == "es_get_ingest_pipeline":
        return await es.get_ingest_pipeline(args["pipeline_id"])

    # -- Document management -----------------------------------------------
    if name == "es_index_document":
        return await es.index_document(
            index=args["index"],
            document=args["document"],
            doc_id=args.get("doc_id"),
            pipeline=args.get("pipeline"),
        )

    if name == "es_bulk_index":
        return await es.bulk_index(
            index=args["index"],
            documents=args["documents"],
            pipeline=args.get("pipeline"),
        )

    if name == "es_raw_search":
        resp = await es.execute_search(args["index"], args["body"], size=args.get("size", 10))
        return resp

    if name == "es_delete_index":
        if not args.get("confirm", False):
            return {"error": "Set 'confirm': true to delete the index."}
        return await es.delete_index(args["index"])

    raise ValueError(f"Unknown tool: {name}")
