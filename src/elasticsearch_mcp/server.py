# ---------------------------------------------------------------------------
# Server factory
# ---------------------------------------------------------------------------


def create_server(config: Optional[ElasticsearchConfig] = None) -> Server:
    """Create and configure the MCP server instance."""
    if config is None:
        config = ElasticsearchConfig.from_env()

    es = ElasticsearchClient(config)
    server = Server("elasticsearch-semantic-search")

    # -----------------------------------------------------------------------
    # Tool: list_tools (handled by MCP SDK automatically via decorator)
    # -----------------------------------------------------------------------

    @server.list_tools()
    async def list_tools() -> list[types.Tool]:
        return _build_tool_list()

    # -----------------------------------------------------------------------
    # Tool dispatcher
    # -----------------------------------------------------------------------

    @server.call_tool()
    async def call_tool(
        name: str, arguments: dict[str, Any]
    ) -> list[types.TextContent]:
        try:
            result = await _dispatch(name, arguments, es, config)
            return [types.TextContent(type="text", text=json.dumps(result, indent=2))]
        except Exception as exc:
            logger.exception("Tool '%s' failed", name)
            error_payload = {"error": str(exc), "tool": name}
            return [
                types.TextContent(type="text", text=json.dumps(error_payload, indent=2))
            ]

    return server


